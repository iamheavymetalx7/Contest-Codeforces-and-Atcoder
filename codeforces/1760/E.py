def solve():
    n=ii()
    a=lmii()
    
    initial_inversion=0
    cnt=0
    max_pos_one=-1
    min_pos_zero=10**9
    for i in range(n):
        if a[i]==1:
            max_pos_one=max(max_pos_one,i)
            cnt+=1
        if a[i]==0:
            min_pos_zero=min(min_pos_zero,i)
            initial_inversion+=cnt
    # print(initial_inversion)
    
    b=a.copy()

    new_inv=0
    if min_pos_zero<10**9:
        b[min_pos_zero]=1
        cnt=0
        for i in range(n):
            if b[i]==1:
                cnt+=1
            if b[i]==0:
                new_inv+=cnt
    new_inv2=0
   
    if max_pos_one>-1:
        a[max_pos_one]=0
        cnt=0
        for i in range(n):
            if a[i]==1:
                cnt+=1
            if a[i]==0:
                new_inv2+=cnt
    
    print(max(new_inv,new_inv2,initial_inversion))
    
    
t=ii()
for _ in range(t):
    solve()