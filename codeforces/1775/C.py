def solve(t):
    n,x = mii()

    if n==x:
        print(n)
        return
    ind=-1;ans=0

    ss1 = bin(n)[2:]
    ss2 = bin(x)[2:]

    # print(ss1,"ss1")
    # print(ss2,"ss2")

    mx1=max(len(ss1),len(ss2))

    s1="0"*(mx1-len(ss1))+ss1
    s2="0"*(mx1-len(ss2))+ss2

    # print(s1,"s1")
    # print(s2,"s2")


    for i in range(0,len(s1)):
        if s1[i]!=s2[i]:
            ind=i
            break
        if s1[i]=="1":
            z=len(s1)-i-1
            p=pow(2,z)
            ans+=p
    
    for i in range(ind,len(s1)):
        if s2[i]!="0":
            print(-1)
            return

    if ind!=0 and s1[ind-1]=="1":
        print(-1)
        return

    z=len(s1)-ind
    p=pow(2,z)
    ans+=p

    print(ans)
    return
        

