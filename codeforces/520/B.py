'''
 we should get the number n starting 
 from m using the operations "add 1 to the number" 
 and "divide the number by 2 if it is even".
'''
def solve():
    n,m=mii()
    cnt=0
    while n<m:
        if m%2:
            m+=1
            cnt+=1
        else:
            m//=2
            cnt+=1
    print(cnt+(n-m))
    
    
def main():
    solve()
 
 
