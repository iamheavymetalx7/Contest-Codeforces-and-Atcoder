def solve():
    n=ii()
    x=lmii()
    arr=[0]*(10**7+1)
    for i in x:
        arr[i]+=1
    sieve=[0,0]+[-1 for _ in range(10**7-1)]

    for i in range(2,10**7+1):
        if sieve[i]==-1:
            sieve[i]=0
            for j in range(i, 10**7+1,i):
                sieve[j]=0
                sieve[i]+=arr[j]

    ## now we create prefix array using the sieve itself

    for i in range(1,len(sieve)):
        sieve[i]+=sieve[i-1]
    
    que = ii()
    for _ in range(que):
        l,r=mii()
        l=min(l,10**7)
        r=min(r,10**7)
        print(sieve[r]-sieve[l-1])


    
    
    
